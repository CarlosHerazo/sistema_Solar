from googletrans import Translator

class Planeta(Translator):
    def __init__(self,nombre,numero_satelites,masa_kg,volumen,diametro,distancia_media_sol,):

        self.nombre = nombre
        self.numero_satelites =numero_satelites
        self.masa_kg = masa_kg
        self.volumen = volumen
        self.diametro = diametro
        self.distancia_media_sol = distancia_media_sol









